// 자바스크립트의 대표적인 자료형

// number type
let n1 = 1234;
let n2 = 5.678;
console.log(n1,n2);

//String type
let s1 = "hello";
let s2 = "world";
console.log(s1,s2);

// Boolean type
let b1 = true;
let b2 = false;
console.log(b1, b2);

// null type
let n = null;
console.log(n);

// Undefined type : 값이 없음
let u1;
let u2 = undefined;
console.log(u1, u2);

// Array type
let arr = [1,2,3,4];
console.log(arr, arr[0]);
// index : 상대적인 위치

// Object type
let obj = {a:"apple", b:"banana", c:"carrot"};
console.log(obj, obj.a, obj['b'], obj["c"]);